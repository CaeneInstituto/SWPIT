// ─── Interfaces ───────────────────────────────────────────────────────────────

export interface Activity {
  time: string
  description: string
  icon?: 'food' | 'sleep' | 'transport' | 'default'
}

export interface DayItinerary {
  day: number
  title: string
  summary: string
  activities: Activity[]
}

export interface PriceOption {
  label: string
  price: string
  note?: string
}

export interface BoardingPoint {
  name: string
  address: string
  time: string
}

export interface Tour {
  id: string
  name: string
  region: string
  location: string
  days: string
  price: string
  priceValue: number
  image: string
  images?: string[]        // carrusel de fotos del tour
  brochure?: string        // ruta al PDF del paquete (ej: '/brochures/huancaya.pdf')
  tag: string
  rating: number
  reviewCount: number
  groupSize: string
  itinerary: DayItinerary[]
  includes: string[]
  notIncludes?: string[]
  notes: string[]
  recommendations?: string[]
  priceOptions?: PriceOption[]
  boardingPoints?: BoardingPoint[]
  departureDays?: string[]
  returnTime?: string
  terms?: string[]
  disabled?: boolean // Para habilitar/deshabilitar paquetes desde admin
  seasons?: string[] // Temporadas en las que está disponible: 'verano', 'invierno', 'primavera', 'otoño', 'semana-santa', 'fiestas-patrias', etc.
  seasonalDiscount?: number // Descuento porcentual para aplicar en temporada activa
  originalPrice?: string // Precio original antes de descuento
  originalPriceValue?: number // Valor original antes de descuento
}

